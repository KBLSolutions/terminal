function getMenuText() {
	return [
		"> STATUS INDEX",
		"",
		...records.map((r) => `[${r.id}] ${r.title}`),
		"",
		'ENTER RECORD ID OR TYPE "EXIT"'
	];
}

export default async function status(selectedId) {
	clear();

	if (selectedId) {
		const directRecord = records.find((r) => r.id === String(selectedId));
		if (!directRecord) {
			await type(`Record not found: ${selectedId}`);
			return;
		}
		await type(directRecord.text, {
			initialWait: 150,
			finalWait: 150
		});
		return;
	}

	await type(getMenuText(), {
		lineWait: 150,
		initialWait: 150,
		finalWait: 150
	});

	let answer = await prompt("SELECT RECORD:");
	answer = answer.trim().toLowerCase();

	if (answer === "exit") {
		clear();
		await type([
			"CLOSING STATUS DATABASE...",
			"RETURNING TO MAIN TERMINAL..."
		], {
			lineWait: 150,
			initialWait: 100,
			finalWait: 200
		});
		return;
	}

	const record = records.find((r) => r.id === answer);

	if (!record) {
		await type("INVALID RECORD ID");
		return;
	}

	clear();
	await type(record.text, {
		initialWait: 150,
		finalWait: 150
	});
}
